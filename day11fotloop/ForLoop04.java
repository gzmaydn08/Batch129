package day11fotloop;

public class ForLoop04 {
    public static void main(String[] args) {

        //Example 1: 5 den 8 e kadar tam sayıların toplamını veren kodu yazınız ÖNEMLİ!!!!!!!
        //    5+6+7+8+==>26

        int sum =0;

        for (int i=5; i<9 ; i++){
            sum = sum + i;


        }
        System.out.println(sum);

    }
}
